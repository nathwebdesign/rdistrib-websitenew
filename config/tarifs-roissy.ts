// Fonction pour obtenir un tarif spécifique
export function getTarifRoissy(
  zone: string,
  typeTransport: 'palette80x120' | 'palette100x120' | 'metrePlancher',
  quantity: number
): number | undefined {
  const tarif = tarifsRoissy.find(t => t.zone === zone);
  if (!tarif) return undefined;
  
  return tarif[typeTransport][quantity];
}

